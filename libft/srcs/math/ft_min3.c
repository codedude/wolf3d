#include "libft.h"

t_i32		ft_min3(t_i32 a, t_i32 b, t_i32 c)
{
	if (a < b)
	{
		if (a < c)
			return (a);
	}
	else
	{
		if (b < c)
			return (b);
	}
	return (c);
}
