#include "libft.h"

int			ft_isprime(t_u32 n)
{
	t_i64	i;
	t_i64	w;

	if (n < 2)
		return (0);
	if (n < 4)
		return (1);
	if (n % 2 == 0 || n % 3 == 0)
		return (0);
	i = 5;
	w = 2;
	while (i * i <= n)
	{
		if (n % i == 0)
			return (0);
		i += w;
		w = 6 - w;
	}
	return (1);
}
