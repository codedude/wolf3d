#include "libft.h"

void	ft_strrev(char *s)
{
	size_t	i;
	size_t	j;
	char	t;

	if (s == NULL)
		return ;
	j = ft_strlen(s);
	if (j > 0)
		j--;
	i = 0;
	while (i < j)
	{
		t = s[i];
		s[i++] = s[j];
		s[j--] = t;
	}
}
